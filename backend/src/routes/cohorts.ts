/**
 * POST /api/cohorts
 * Create a new cohort
 */
router.post(
  '/',
  authenticate,
  userRateLimiter,
  asyncHandler(async (req: Request, res: Response) => {
    const { name, definition, platform, country, planType, signupSource } = req.body;

    if (!name || !definition) {
      throw new BadRequestError('Name and definition are required');
    }

    // TODO: Check if user is admin

    // Find users matching criteria
    const userIds = await findUsersMatchingCriteria(definition as CohortDefinition);

    const cohort = await prisma.userCohort.create({
      data: {
        name,
        definition: definition as CohortDefinition,
        userIds,
        platform: platform || null,
        country: country || null,
        planType: planType || null,
        signupSource: signupSource || null,
      },
    });

    res.json({
      success: true,
      data: {
        id: cohort.id,
        name: cohort.name,
        userCount: cohort.userIds.length,
      },
    });
  })
);

/**
 * PUT /api/cohorts/:id
 * Update cohort
 */
router.put(
  '/:id',
  authenticate,
  userRateLimiter,
  asyncHandler(async (req: Request, res: Response) => {
    const { id } = req.params;
    const { name, definition } = req.body;

    // TODO: Check if user is admin

    const cohort = await prisma.userCohort.findUnique({
      where: { id },
    });

    if (!cohort) {
      throw new NotFoundError('Cohort not found');
    }

    let userIds = cohort.userIds;
    if (definition) {
      userIds = await findUsersMatchingCriteria(definition as CohortDefinition);
    }

    const updated = await prisma.userCohort.update({
      where: { id },
      data: {
        ...(name && { name }),
        ...(definition && { definition: definition as CohortDefinition }),
        userIds,
      },
    });

    res.json({
      success: true,
      data: updated,
    });
  })
);


/**
 * GET /api/cohorts/:id/predictions
 * Get predictive analytics for a cohort
 */
router.get(
  '/:id/predictions',
  authenticate,
  userRateLimiter,
  asyncHandler(async (req: Request, res: Response) => {
    const { id } = req.params;
    const { method = 'combined', weeksAhead = 4 } = req.query;

    const cohortData = await getCohortRetentionData(id);
    const retentionData = cohortData.retentionByWeek.filter(r => r > 0);

    if (retentionData.length < 2) {
      return res.json({
        success: true,
        data: [],
        message: 'Not enough historical data for predictions',
      });
    }

    let predictions;
    switch (method) {
      case 'linear':
        predictions = linearRegressionPrediction(retentionData, Number(weeksAhead));
        break;
      case 'moving_average':
        predictions = movingAveragePrediction(retentionData, 3, Number(weeksAhead));
        break;
      case 'ml':
        predictions = mlPrediction(retentionData, 0.3, Number(weeksAhead));
        break;
      default:
        predictions = combinedPrediction(retentionData, Number(weeksAhead));
    }

    res.json({
      success: true,
      data: {
        historical: retentionData,
        predictions,
        method: method as string,
      },
    });
  })
);

/**
 * GET /api/cohorts/compare
 * Compare two cohorts side-by-side
 */
router.get(
  '/compare',
  authenticate,
  userRateLimiter,
  asyncHandler(async (req: Request, res: Response) => {
    const { cohort1Id, cohort2Id } = req.query;

    if (!cohort1Id || !cohort2Id) {
      throw new BadRequestError('cohort1Id and cohort2Id are required');
    }

    const [cohort1, cohort2] = await Promise.all([
      getCohortRetentionData(cohort1Id as string),
      getCohortRetentionData(cohort2Id as string),
    ]);

    res.json({
      success: true,
      data: {
        cohort1,
        cohort2,
      },
    });
  })
);

/**
 * GET /api/cohorts/:id/export
 * Export cohort data as CSV/Excel
 */
router.get(
  '/:id/export',
  authenticate,
  userRateLimiter,
  asyncHandler(async (req: Request, res: Response) => {
    const { id } = req.params;
    const { format = 'csv' } = req.query;

    const cohortData = await getCohortRetentionData(id);
    const cohort = await prisma.userCohort.findUnique({
      where: { id },
    });

    if (!cohort) {
      throw new NotFoundError('Cohort not found');
    }

    if (format === 'csv') {
      // Generate CSV
      const csvRows: string[] = [];
      
      // Header
      csvRows.push('Week,Active Users,New Users,Churned Users,Avg Watch Time (min),Avg Revenue,Retention %');
      
      // Data rows
      cohortData.metrics.forEach((metric, index) => {
        csvRows.push(
          `${index + 1},${metric.activeUsers},${metric.newUsers},${metric.churnedUsers},${Math.round(metric.avgWatchTime / 60)},${metric.avgRevenue.toFixed(2)},${(metric.retention * 100).toFixed(2)}`
        );
      });

      const csv = csvRows.join('\n');
      
      res.setHeader('Content-Type', 'text/csv');
      res.setHeader('Content-Disposition', `attachment; filename="cohort-${cohort.name}-${Date.now()}.csv"`);
      res.send(csv);
    } else {
      // For Excel, we'd use a library like exceljs
      // For now, return JSON
      res.json({
        success: true,
        data: {
          cohort: {
            name: cohort.name,
            userCount: cohort.userIds.length,
            platform: cohort.platform,
            country: cohort.country,
            planType: cohort.planType,
            signupSource: cohort.signupSource,
          },
          metrics: cohortData.metrics,
        },
        message: 'Excel export not yet implemented. Use CSV format.',
      });
    }
  })
);

